window.onload = () => {
    generalOnload()
    addHeader(false)
    addSidebar()

    // Get the author name from the URL
    urlSearchParams = new URLSearchParams(window.location.search)
    if (!urlSearchParams.has("author")) {
        // No author specified
        // Send the user to the error 404 page
        window.location.href = "./404.html"
    }
    else {
        let author = urlSearchParams.get("author")
        if (!authorData[author]) {
            // Invalid author
            // Send the user to the error 404 page
            window.location.href = "./404.html"
        }
        else {
            // Author exists, add the author page
            addAuthor(authorData[author])
        }
    }
}

// Add the author page
function addAuthor(data) {
    console.log(data)
    $('#scripts').load('./assets/elements/author.html', function () {
        console.log(document.getElementById("author-header-template"))
        var authorHeader = Handlebars.compile(document.getElementById("author-header-template").innerHTML)
        document.getElementById("author-header").innerHTML = authorHeader(data)
        var authorabout = Handlebars.compile(document.getElementById("author-about-template").innerHTML)
        document.getElementById("author-info").innerHTML = authorabout(data)
    })

    // Add the author's articles
    addNewsList(data.name + "'s Articles", data.articles)
}